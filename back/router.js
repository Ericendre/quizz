router.get('/search/:id', (req, res) => {
  let request01 = "SELECT * FROM users WHERE login LIKE '%"+ req.params.id +"%'"
  let request02 = "SELECT * FROM quizz WHERE name LIKE '%"+ req.params.id +"%'"
  db.all( request01, [], (err, rows) => {
    let result = [rows];
    db.all( request02, [], (err, rows) => {
      result.push(rows);
      res.json(result);
    });
  });
});




router.post('/upload', (req, res) => {
    if (!req.files)
        return res.status(500).send({ msg: "file is not found" })

    // accessing the file
    const myFile = req.files.file;


    //  mv() method places the file inside public directory
    myFile.mv(`${__dirname}/data/img/${myFile.name}`, function (err) {
        if (err) {
            console.log(err)
            return res.status(500).send({ msg: "Error occured" });
        }
        // returing the response with file path and name
        console.log(myFile.name);
        return res.send({name: myFile.name});
    });
});



